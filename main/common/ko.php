<p>&nbsp; ④ 전항의 경우 &ldquo;학회 홈페이지&rdquo;는 이로 인하여 이용자가 입은 손해를 배상합니다. 다만, &ldquo;학회 홈페이지&rdquo;가 고의 또는 과실이 없음을 입증하는 경우에는 그러하지 아니합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제5조(서비스의 중단) </span></p>

<p>&nbsp; ① &ldquo;학회 홈페이지&rdquo;는 컴퓨터 등 정보통신설비의 보수점검&middot;교체 및 고장, 통신의 두절 등의 사유가 발생한 경우에는 서비스의 제공을 일시적으로 중단할 수 있습니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;는 제1항의 사유로 서비스의 제공이 일시적으로 중단됨으로 인하여 이용자 또는 제3자가 입은 손해에 대하여 배상합니다. 단, &ldquo;학회 홈페이지&rdquo;가 고의 또는 과실이 없음을 입증하는 경우에는 그러하지 아니합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ③ 사업종목의 전환, 사업의 포기, 업체 간의 통합 등의 이유로 서비스를 제공할 수 없게 되는 경우에는 &ldquo;학회 홈페이지&rdquo;는 제8조에 정한 방법으로 이용자에게 통지하고 당초 &ldquo;학회 홈페이지&rdquo;에서 제시한 조건에 따라 소비자에게 보상합니다. 다만, &ldquo;학회 홈페이지&rdquo;가 보상기준 등을 고지하지 아니한 경우에는 이용자들의 마일리지 또는 적립금 등을 &ldquo;학회 홈페이지&rdquo;에서 통용되는 통화가치에 상응하는 현물 또는 현금으로 이용자에게 지급합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제6조(회원가입) </span></p>

<p>&nbsp; ① 이용자는 &ldquo;학회 홈페이지&rdquo;가 정한 가입 양식에 따라 회원정보를 기입한 후 이 약관에 동의한다는 의사표시를 함으로서 회원가입을 신청합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;는 제1항과 같이 회원으로 가입할 것을 신청한 이용자 중 다음 각 호에 해당하지 않는 한 회원으로 등록합니다.</p>

<p>&nbsp;&nbsp;&nbsp; 1. 가입신청자가 이 약관 제7조제3항에 의하여 이전에 회원자격을 상실한 적이 있는 경우, 다만 제7조제3항에 의한 회원자격 상실 후 3년이 경과한 자로서 &ldquo;&ldquo;학회 홈페이지&rdquo;의 회원재가입 승낙을 얻은 경우에는 예외로 한다.</p>

<p>&nbsp;&nbsp;&nbsp; 2. 등록 내용에 허위, 기재누락, 오기가 있는 경우</p>

<p>&nbsp;&nbsp;&nbsp; 3. 기타 회원으로 등록하는 것이 &ldquo;학회 홈페이지&rdquo;의 기술상 현저히 지장이 있다고 판단되는 경우</p>

<p>&nbsp;</p>

<p>&nbsp; ③ 회원가입계약의 성립 시기는 &ldquo;학회 홈페이지&rdquo;의 승낙이 회원에게 도달한 시점으로 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ④ 회원은 회원가입 시 등록한 사항에 변경이 있는 경우, 상당한 기간 이내에 &ldquo;학회 홈페이지&rdquo;에 대하여 회원정보 수정 등의 방법으로 그 변경사항을 알려야 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제7조(회원 탈퇴 및 자격 상실 등) </span></p>

<p>&nbsp; ① 회원은 &ldquo;학회 홈페이지&rdquo;에 언제든지 탈퇴를 요청할 수 있으며 &ldquo;학회 홈페이지&rdquo;는 즉시 회원탈퇴를 처리합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② 회원이 다음 각 호의 사유에 해당하는 경우, &ldquo;학회 홈페이지&rdquo;는 회원자격을 제한 및 정지시킬 수 있습니다.</p>

<p>&nbsp;&nbsp;&nbsp; 1. 가입 신청 시에 허위 내용을 등록한 경우</p>

<p>&nbsp;&nbsp;&nbsp; 2. &ldquo;학회 홈페이지&rdquo;를 이용하여 구입한 재화 등의 대금, 기타 &ldquo;학회 홈페이지&rdquo;이용에 관련하여 회원이 부담하는 채무를 기일에 지급하지 않는 경우</p>

<p>&nbsp;&nbsp;&nbsp; 3. 다른 사람의 &ldquo;학회 홈페이지&rdquo; 이용을 방해하거나 그 정보를 도용하는 등 전자상거래 질서를 위협하는 경우</p>

<p>&nbsp;&nbsp;&nbsp; 4. &ldquo;학회 홈페이지&rdquo;를 이용하여 법령 또는 이 약관이 금지하거나 공서양속에 반하는 행위를 하는 경우</p>

<p>&nbsp;</p>

<p>&nbsp; ③ &ldquo;학회 홈페이지&rdquo;가 회원 자격을 제한&middot;정지 시킨 후, 동일한 행위가 2회 이상 반복되거나 30일 이내에 그 사유가 시정되지 아니하는 경우 &ldquo;학회 홈페이지&rdquo;는 회원자격을 상실시킬 수 있습니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ④ &ldquo;학회 홈페이지&rdquo;가 회원자격을 상실시키는 경우에는 회원등록을 말소합니다. 이 경우 회원에게 이를 통지하고, 회원등록 말소 전에 최소한 30일 이상의 기간을 정하여 소명할 기회를 부여합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제8조(회원에 대한 통지)</span></p>

<p>&nbsp; ① &ldquo;학회 홈페이지&rdquo;가 회원에 대한 통지를 하는 경우, 회원이 &ldquo;학회 홈페이지&rdquo;와 미리 약정하여 지정한 전자우편 주소로 할 수 있습니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;는 불특정다수 회원에 대한 통지의 경우 1주일이상 &ldquo;학회 홈페이지&rdquo; 게시판에 게시함으로서 개별 통지에 갈음할 수 있습니다. 다만, 회원 본인의 거래와 관련하여 중대한 영향을 미치는 사항에 대하여는 개별통지를 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제9조(구매신청 및 개인정보 제공 동의 등) </span></p>

<p>&nbsp; ① &ldquo;학회 홈페이지&rdquo;이용자는 &ldquo;학회 홈페이지&rdquo;상에서 다음 또는 이와 유사한 방법에 의하여 구매를 신청하며, &ldquo;학회 홈페이지&rdquo;는 이용자가 구매신청을 함에 있어서 다음의 각 내용을 알기 쉽게 제공하여야 합니다.</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 1. 재화 등의 검색 및 선택</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 2. 받는 사람의 성명, 주소, 전화번호, 전자우편주소(또는 이동전화번호) 등의 입력</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 3. 약관내용, 청약철회권이 제한되는 서비스, 배송료&middot;설치비 등의 비용부담과 관련한 내용에 대한 확인</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 4. 이 약관에 동의하고 위 3.호의 사항을 확인하거나 거부하는 표시</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; (예, 마우스 클릭)</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 5. 재화등의 구매신청 및 이에 관한 확인 또는 &ldquo;학회 홈페이지&rdquo;의 확인에 대한 동의</p>

<p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; 6. 결제방법의 선택</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;가 제3자에게 구매자 개인정보를 제공할 필요가 있는 경우&nbsp; 1) 개인정보를 제공받는 자, 2)개인정보를 제공받는 자의 개인정보 이용목적, 3) 제공하는 개인정보의 항목, 4) 개인정보를 제공받는 자의 개인정보 보유 및 이용기간을 구매자에게 알리고 동의를 받아야 합니다. (동의를 받은 사항이 변경되는 경우에도 같습니다.)</p>

<p>&nbsp;</p>

<p>&nbsp; ③ &ldquo;학회 홈페이지&rdquo;가 제3자에게 구매자의 개인정보를 취급할 수 있도록 업무를 위탁하는 경우에는 1) 개인정보 취급위탁을 받는 자, 2) 개인정보 취급위탁을 하는 업무의 내용을 구매자에게 알리고 동의를 받아야 합니다. (동의를 받은 사항이 변경되는 경우에도 같습니다.) 다만, 서비스제공에 관한 계약이행을 위해 필요하고 구매자의 편의증진과 관련된 경우에는 「정보통신망 이용촉진 및 정보보호 등에 관한 법률」에서 정하고 있는 방법으로 개인정보 취급방침을 통해 알림으로써 고지절차와 동의절차를 거치지 않아도 됩니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제10조 (계약의 성립)</span></p>

<p>&nbsp; ①&nbsp; &ldquo;학회 홈페이지&rdquo;는 제9조와 같은 구매신청에 대하여 다음 각 호에 해당하면 승낙하지 않을 수 있습니다. 다만, 미성년자와 계약을 체결하는 경우에는 법정대리인의 동의를 얻지 못하면 미성년자 본인 또는 법정대리인이 계약을 취소할 수 있다는 내용을 고지하여야 합니다.</p>

<p>&nbsp;&nbsp;&nbsp; 1. 신청 내용에 허위, 기재누락, 오기가 있는 경우</p>

<p>&nbsp;&nbsp;&nbsp; 2. 미성년자가 담배, 주류 등 청소년보호법에서 금지하는 재화 및 용역을 구매하는 경우</p>

<p>&nbsp;&nbsp;&nbsp; 3. 기타 구매신청에 승낙하는 것이 &ldquo;학회 홈페이지&rdquo; 기술상 현저히 지장이 있다고 판단하는 경우</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;의 승낙이 제12조제1항의 수신확인통지형태로 이용자에게 도달한 시점에 계약이 성립한 것으로 봅니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ③ &ldquo;학회 홈페이지&rdquo;의 승낙의 의사표시에는 이용자의 구매 신청에 대한 확인 및 판매가능 여부, 구매신청의 정정 취소 등에 관한 정보 등을 포함하여야 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제11조(지급방법) </span></p>

<p>&ldquo;학회 홈페이지&rdquo;에서 구매한 재화 또는 용역에 대한 대금지급방법은 다음 각 호의 방법중 가용한 방법으로 할 수 있습니다. 단, &ldquo;학회 홈페이지&rdquo;는 이용자의 지급방법에 대하여 재화 등의 대금에 어떠한 명목의 수수료도 추가하여 징수할 수 없습니다.</p>

<p>&nbsp;&nbsp;&nbsp; 1. 폰뱅킹, 인터넷뱅킹, 메일 뱅킹 등의 각종 계좌이체</p>

<p>&nbsp;&nbsp;&nbsp; 2. 선불카드, 직불카드, 신용카드 등의 각종 카드 결제</p>

<p>&nbsp;&nbsp;&nbsp; 3. 온라인무통장입금</p>

<p>&nbsp;&nbsp;&nbsp; 4. 전자화폐에 의한 결제</p>

<p>&nbsp;&nbsp;&nbsp; 5. 수령 시 대금지급</p>

<p>&nbsp;&nbsp;&nbsp; 6. 마일리지 등 &ldquo;학회 홈페이지&rdquo;가 지급한 포인트에 의한 결제</p>

<p>&nbsp;&nbsp;&nbsp; 7. &ldquo;학회 홈페이지&rdquo;와 계약을 맺었거나 &ldquo;학회 홈페이지&rdquo;가 인정한 상품권에&nbsp; 의한 결제&nbsp;</p>

<p>&nbsp;&nbsp;&nbsp; 8. 기타 전자적 지급 방법에 의한 대금 지급 등</p>

<p>&nbsp;&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제12조(수신확인통지&middot;구매신청 변경 및 취소)</span></p>

<p>&nbsp; ① &ldquo;학회 홈페이지&rdquo;는 이용자의 구매신청이 있는 경우 이용자에게 수신확인통지를 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② 수신확인통지를 받은 이용자는 의사표시의 불일치 등이 있는 경우에는 수신확인통지를 받은 후 즉시 구매신청 변경 및 취소를 요청할 수 있고 &ldquo;학회 홈페이지&rdquo;는 배송 전에 이용자의 요청이 있는 경우에는 지체 없이 그 요청에 따라 처리하여야 합니다. 다만 이미 대금을 지불한 경우에는 제15조의 청약철회 등에 관한 규정에 따릅니다.</p>

<p>&nbsp;</p>

<p>&nbsp;</p>

<p><span style='font-size:20px;font-weight:bold;'>제13조(재화 등의 공급)</span></p>

<p>&nbsp; ① &ldquo;학회 홈페이지&rdquo;는 이용자와 재화 등의 공급시기에 관하여 별도의 약정이 없는 이상, 이용자가 청약을 한 날부터 7일 이내에 재화 등을 배송할 수 있도록 주문제작, 포장 등 기타의 필요한 조치를 취합니다. 다만, &ldquo;학회 홈페이지&rdquo;가 이미 재화 등의 대금의 전부 또는 일부를 받은 경우에는 대금의 전부 또는 일부를 받은 날부터 3영업일 이내에 조치를 취합니다.&nbsp; 이때 &ldquo;학회 홈페이지&rdquo;는 이용자가 재화 등의 공급 절차 및 진행 사항을 확인할 수 있도록 적절한 조치를 합니다.</p>

<p>&nbsp;</p>

<p>&nbsp; ② &ldquo;학회 홈페이지&rdquo;는 이용자가 구매한 재화에 대해 배송수단, 수단별 배송비용 부담자, 수단별 배송기간 등을 명시합니다. 만약 &ldquo;학회 홈페이지&rdquo;가 약정 배송기간을 초과한 경우에는 그로 인한 이용자의 손해를 배상하여야 합니다. 다만 &ldquo;학회 홈페이지&rdquo;가 고의&middot;과실이 없음을 입증한 경우에는 그러하지 아니합니다.</p>

<p>&nbsp;</p>

